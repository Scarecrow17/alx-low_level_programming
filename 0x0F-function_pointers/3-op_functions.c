#include "3-calc.h"

/**
 * op_add - calculate sum two integers
 * @a: integer one
 * @b: integer two
 * Return: a and b sum result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate difference between two integers
 * @a: integer one
 * @b: integer two
 * Return: difference between a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate product of two integers
 * @a: integer one
 * @b: integer two
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate quotient of two integers
 * @a: integer one
 * @b: integer two
 * Return: result of dividing
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate remainder of division of two integers
 * @a: integer one
 * @b: integer two
 * Return: remainder of quotient
 */

int op_mod(int a, int b)
{
	return (a % b);
}
